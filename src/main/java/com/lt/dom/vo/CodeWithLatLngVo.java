public class CodeWithLatLngVo {

    private String c;
    private float st;
    private float sg;
    private float ct;
    private float cg;

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }

    public float getSt() {
        return st;
    }

    public void setSt(float st) {
        this.st = st;
    }

    public float getSg() {
        return sg;
    }

    public void setSg(float sg) {
        this.sg = sg;
    }

    public float getCt() {
        return ct;
    }

    public void setCt(float ct) {
        this.ct = ct;
    }

    public float getCg() {
        return cg;
    }

    public void setCg(float cg) {
        this.cg = cg;
    }
}
